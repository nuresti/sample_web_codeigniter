<script type="text/javascript">
    $(document).ready(function(){
        show_kategori(); //call function show all product
         
        $('#mydata').dataTable();
          
        //function show all product
        function show_kategori(){
            $.ajax({
                type  : 'ajax',
                url   : '<?php echo site_url('c_kategori/show_kategori')?>',
                async : true,
                dataType : 'json',
                success : function(data){
                    var html = '';
                    var i;
                    for(i=0; i<data.length; i++){
                        html += '<tr>'+
                                '<td>'+data[i].id+'</td>'+
                                '<td>'+data[i].nama_kategori+'</td>'+
                                '<td>'+data[i].deskripsi+'</td>'+
                                '<td style="text-align:right;">'+
                                    '<a href="javascript:void(0);" class="btn btn-info btn-sm item_edit" data-id="'+data[i].id+'" data-nama_kategori="'+data[i].nama_kategori+'" data-deskripsi="'+data[i].deskripsi+'">Edit</a>'+' '+
                                    '<a href="javascript:void(0);" class="btn btn-danger btn-sm item_delete" data-id="'+data[i].id+'">Delete</a>'+
                                '</td>'+
                                '</tr>';
                    }
                    $('#show_data').html(html);
                }
 
            });
        }
 
        //Save product
        $('#btn_save').on('click',function(){
            var id = $('#id').val();
            var nama_kategori = $('#nama_kategori').val();
            var deskripsi        = $('#deskripsi').val();
            $.ajax({
                type : "POST",
                url  : "<?php echo site_url('c_kategori/add_kategori')?>",
                dataType : "JSON",
                data : {id:id , nama_kategori:nama_kategori, deskripsi:deskripsi},
                success: function(data){
                    $('[name="id"]').val("");
                    $('[name="nama_kategori"]').val("");
                    $('[name="deskripsi"]').val("");
                    $('#basic').modal('hide');
                    show_kategori();
                }
            });
            return false;
        });
 
        //get data for update record
        $('#show_data').on('click','.item_edit',function(){
            var id = $(this).data('id');
            var nama_kategori = $(this).data('nama_kategori');
            var dekkripsi        = $(this).data('deskripsi');
             
            $('#edit').modal('show');
            $('[name="id"]').val(id);
            $('[name="nama_kategori"]').val(nama_kategori);
            $('[name="deskripsi"]').val(deskripsi);
        });
 
        //update record to database
         $('#btn_update').on('click',function(){
            var id = $('#id').val();
            var nama_kategori = $('#nama_kategori').val();
            var deskripsi        = $('#deskripsi').val();
            $.ajax({
                type : "POST",
                url  : "<?php echo site_url('c_kategori/update')?>",
                dataType : "JSON",
                data : {id:id , nama_kategori:nama_kategori, deskripsi:deskripsi},
                success: function(data){
                    $('[name="id"]').val("");
                    $('[name="nama_kategori"]').val("");
                    $('[name="deskripsi"]').val("");
                    $('#edit').modal('hide');
                    show_kategori();
                }
            });
            return false;
        });
 
        //get data for delete record
        $('#show_data').on('click','.item_delete',function(){
            var id = $(this).data('id');
             
            $('#delete').modal('show');
            $('[name="id"]').val(id);
        });
 
        //delete record to database
         $('#btn_delete').on('click',function(){
            var product_code = $('#id').val();
            $.ajax({
                type : "POST",
                url  : "<?php echo site_url('c_kategori/delete')?>",
                dataType : "JSON",
                data : {id:id},
                success: function(data){
                    $('[name="id"]').val("");
                    $('#delete').modal('hide');
                    show_kategori();
                }
            });
            return false;
        });
 
    });
 
</script>
